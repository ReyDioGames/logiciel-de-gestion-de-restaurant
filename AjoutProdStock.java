    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
        Connection con;
        PreparedStatement pst;
        PreparedStatement prepared;
        ResultSet rs;
        
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() ){
        jLabel4.setVisible(true);
        jLabel4.setText("Informations Manquantes");
        jLabel4.setForeground(Color.red);
        }else{
        
            try {
                
                String vc = jComboBox1.getSelectedItem().toString();
                String vnp = jTextField1.getText().toString();
                int vp = Integer.parseInt(jTextField2.getText());
                int vpa = Integer.parseInt(jTextField3.getText());
                
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/restocafe","root","");
                
                    int vid=1;
                    String rid = "Select max(IdProd) from produit";
                    prepared = con.prepareStatement(rid);
                    rs = prepared.executeQuery();
                    while(rs.next()){
                    vid = rs.getInt("max(IdProd)")+1;
                    }
                
                
                String requete = "INSERT INTO produit(IdProd,Produit,PrixAchat,Prix,Categorie) value(?,?,?,?,?)";
                pst = con.prepareStatement(requete);
                pst.setInt(1,vid);
                pst.setString(2,vnp);
                pst.setInt(3,vpa);
                pst.setInt(4,vp);
                pst.setString(5,vc);
                

                pst.executeUpdate();
               gs.affProd();
                
                this.dispose();
            } catch (Exception e) {
                
            }
        
        }


    }//GEN-LAST:event_jButton1ActionPerformed

