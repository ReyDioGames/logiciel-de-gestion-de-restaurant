    public void table(){
    try {
        String columns[] = { "Produits", "Prix Unité" , "Quantité", "Prix Total" };
        String data[][] = new String[50][4];
        Connection con;
        PreparedStatement prepared;
        ResultSet rs;
        Class.forName("com.mysql.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost/restocafe","root","");
        String requete = "Select * from table"+choix+"";
        prepared = con.prepareStatement(requete);
        rs = prepared.executeQuery();
        int i = 0;
        int total = 0;
        while(rs.next()){
              String nomprod = rs.getString("Produit");
              String qte = rs.getString("Qte");
              String prix = rs.getString("Prix");
              String prt = String.valueOf(Integer.parseInt(qte)*Integer.parseInt(prix));
              data[i][0] = nomprod;
              data[i][1] = prix;
              data[i][2] = qte;
              data[i][3] = prt;
              total = total+Integer.parseInt(prt);
              i++;
        }
        DefaultTableModel model = new DefaultTableModel(data, columns);
        jTable1.setModel(model);
        jTextField1.setText("Total : "+total+" DA");
        
        
    } catch (Exception e) {
        
    }
    
    }
    
